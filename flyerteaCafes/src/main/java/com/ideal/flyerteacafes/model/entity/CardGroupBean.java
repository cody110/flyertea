public class CardGroupBean {

    private int cardChannelId;
    private String cardChannelName;
    private String cardChannelNameEn;
    private String cardChannelLogoUrl;

    public int getCardChannelId() {
        return cardChannelId;
    }

    public void setCardChannelId(int cardChannelId) {
        this.cardChannelId = cardChannelId;
    }

    public String getCardChannelName() {
        return cardChannelName;
    }

    public void setCardChannelName(String cardChannelName) {
        this.cardChannelName = cardChannelName;
    }

    public String getCardChannelNameEn() {
        return cardChannelNameEn;
    }

    public void setCardChannelNameEn(String cardChannelNameEn) {
        this.cardChannelNameEn = cardChannelNameEn;
    }

    public String getCardChannelLogoUrl() {
        return cardChannelLogoUrl;
    }

    public void setCardChannelLogoUrl(String cardChannelLogoUrl) {
        this.cardChannelLogoUrl = cardChannelLogoUrl;
    }
}
